function updateTranslation() {
    const selectedLanguage = document.getElementById('language-select').value;
    const translation = translations[selectedLanguage];

    if (translation) {
        document.title = translation.title;
        
        document.querySelector('.rate-button').textContent = translation.btnRate;
        document.querySelector('.main-text h1').textContent = translation.exchangeRatesWidget;
        document.querySelector('.main-text p').textContent = translation.checkRates;
        document.querySelector('.you-have p').textContent = translation.youHave;
        document.querySelector('.you-have-to p').textContent = translation.to;
        document.querySelector('.you-get p').textContent = translation.youGet;
        document.querySelector('.you-get-to p').textContent = translation.toP;
        document.querySelector('.you-get-to p').textContent = translation.toP;
        document.querySelector('.converter-button a').textContent = translation.btnConvert;
        document.querySelector('.info-container p').textContent = translation.infoContainer;

        const languageOptions = document.querySelectorAll('.select-language option');
        languageOptions.forEach(option => {
            option.textContent = translation[option.value];
        });
    }
}

updateTranslation();

const languageSelect = document.getElementById('language-select');
languageSelect.addEventListener('change', updateTranslation);